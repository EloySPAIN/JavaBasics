public class JavaBasic1App {

	public static void main(String[] args) {
		int x=17;
		int y=20;
		int suma=x+y;
		int resta=x-y;
		int multi=x*y;
		int div=x/y;
		int mod=x%y;
		System.out.println("Suma de "+x+"+"+ y+" es:"+suma);
		System.out.println("Resta de "+x+"-"+ y+" es:"+resta);
		System.out.println("Multiplicación de "+x+"*"+ y+" es:"+multi);
		System.out.println("División de "+x+"/"+ y+" es:"+div);
		System.out.println("Módulo de "+x+"%"+ y+" es:"+mod);
	}

}
